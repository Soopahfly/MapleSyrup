// ── Low-level I2C helpers ─────────────────────────────────────────────────────
static void ssd1306_cmd(uint8_t cmd) {
    uint8_t buf[2] = { 0x00, cmd };
    i2c_write_blocking(OLED_I2C, OLED_I2C_ADDR, buf, 2, false);
}

static void ssd1306_data(const uint8_t *data, uint16_t len) {
    // Control byte 0x40 = data stream
    uint8_t hdr = 0x40;
    i2c_write_blocking(OLED_I2C, OLED_I2C_ADDR, &hdr, 1, true);
    i2c_write_blocking(OLED_I2C, OLED_I2C_ADDR, data, len, false);
}

// ── Flush framebuffer to display ──────────────────────────────────────────────
static void flush(void) {
    ssd1306_cmd(SSD1306_COLUMN_ADDR); ssd1306_cmd(0); ssd1306_cmd(OLED_WIDTH - 1);
    ssd1306_cmd(SSD1306_PAGE_ADDR);   ssd1306_cmd(0); ssd1306_cmd((OLED_HEIGHT / 8) - 1);
    ssd1306_data(g_fb, sizeof(g_fb));
}

// ── Public API ────────────────────────────────────────────────────────────────
void oled_init(void) {
    i2c_init(OLED_I2C, 400000);
    gpio_set_function(PIN_OLED_SDA, GPIO_FUNC_I2C);
    gpio_set_function(PIN_OLED_SCL, GPIO_FUNC_I2C);
    gpio_pull_up(PIN_OLED_SDA);
    gpio_pull_up(PIN_OLED_SCL);

    sleep_ms(10);

    ssd1306_cmd(SSD1306_DISPLAY_OFF);
    ssd1306_cmd(SSD1306_SET_DISPLAY_CLK);  ssd1306_cmd(0x80);
    ssd1306_cmd(SSD1306_SET_MULTIPLEX);    ssd1306_cmd(OLED_HEIGHT - 1);
    ssd1306_cmd(SSD1306_SET_DISPLAY_OFFSET); ssd1306_cmd(0x00);
    ssd1306_cmd(SSD1306_SET_START_LINE | 0x00);
    ssd1306_cmd(SSD1306_CHARGE_PUMP);      ssd1306_cmd(0x14);  // internal VCC
    ssd1306_cmd(SSD1306_MEMORY_MODE);      ssd1306_cmd(0x00);  // horizontal
    ssd1306_cmd(SSD1306_SEG_REMAP);
    ssd1306_cmd(SSD1306_COM_SCAN_DEC);
    ssd1306_cmd(SSD1306_SET_COM_PINS);     ssd1306_cmd(0x12);
    ssd1306_cmd(SSD1306_SET_CONTRAST);     ssd1306_cmd(0xCF);
    ssd1306_cmd(SSD1306_SET_PRECHARGE);    ssd1306_cmd(0xF1);
    ssd1306_cmd(SSD1306_SET_VCOM_DETECT);  ssd1306_cmd(0x40);
    ssd1306_cmd(SSD1306_DISPLAY_ALL_ON_RES);
    ssd1306_cmd(SSD1306_NORMAL_DISPLAY);
    ssd1306_cmd(SSD1306_DISPLAY_ON);

    memset(g_fb, 0, sizeof(g_fb));
    flush();
    printf("[oled] init ok\n");
}

void oled_clear(void) {
    memset(g_fb, 0, sizeof(g_fb));
    flush();
}

// The VMU LCD is 48×32, 1bpp, row-major MSB-first (192 bytes).
// Centre it on the 128×64 display: x_offset=40, y_offset=1 page (8px).
void oled_show_vmu(const uint8_t *vmu_pixels) {
    // Clear framebuffer (keep bottom status row)
    memset(g_fb, 0, OLED_WIDTH * 4);  // pages 0-3 = rows 0-31 (offset by 1 page)

    // x centre offset: (128 - 48) / 2 = 40 pixels = 5 bytes
    const int x_off = 40;  // pixel offset
    const int page_off = 1; // start at page 1 (row 8)

    // VMU is row-major; SSD1306 framebuffer is page (column-major) format.
    // Convert: for each row r (0-31), for each column c (0-47):
    //   vmu bit: byte = r*6 + c/8, bit = 7-(c%8)
    //   fb  bit: page = page_off + r/8, col = x_off+c, bit = r%8
    for (int r = 0; r < 32; r++) {
        for (int c = 0; c < 48; c++) {
            int vmu_byte = r * 6 + c / 8;
            int vmu_bit  = 7 - (c % 8);
            int pixel    = (vmu_pixels[vmu_byte] >> vmu_bit) & 1;

            int fb_page  = page_off + r / 8;
            int fb_col   = x_off + c;
            int fb_bit   = r % 8;

            if (pixel)
                g_fb[fb_page * OLED_WIDTH + fb_col] |=  (1u << fb_bit);
            else
                g_fb[fb_page * OLED_WIDTH + fb_col] &= ~(1u << fb_bit);
        }
    }
    flush();
}

void oled_show_status(const char *text) {
    // Draw text on the last page (row 56-63) starting at column 0.
    int page = 7;
    int col  = 0;
    for (int i = 0; text[i] && col + 5 <= OLED_WIDTH; i++) {
        uint8_t ch = (uint8_t)text[i];
        if (ch < 0x20 || ch > 0x7E) ch = '?';
        const uint8_t *glyph = k_font5x7[ch - 0x20];
        for (int x = 0; x < 5; x++)
            g_fb[page * OLED_WIDTH + col + x] = glyph[x];
        g_fb[page * OLED_WIDTH + col + 5] = 0x00;   // spacing
        col += 6;
    }
    flush();
}
